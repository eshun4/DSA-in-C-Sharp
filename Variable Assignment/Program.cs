using System;

namespace usingVariable{
    class Project{
        static void Main(String[] args){
            // Integer variable declaration
            int x = 5;

            // Float variable declaration
            float y = 3.0f;
            float z = -1.0f;
            float num = 0.8f;

            // Variables can also hold words, letters and sentences
            string string1 = "a";
            string string2 = "bird";
            string string3 = "I eat pizza";

            // We can also declare characters
            char char1 = "a";
            char char2 = "b";
            char char3 = "c";

            // We can also declare boolean values
            bool complete = false;
            bool incomplete = true;
        }
    }
}
